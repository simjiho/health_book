package vo;

public class BookVO {
	private int book_id;
	private String user_id;
	private String trainer_name;
	private int book_hour;
	private int book_pay;
	
	public int getBook_id() {
		return book_id;
	}
	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getTrainer_name() {
		return trainer_name;
	}
	public void setTrainer_name(String trainer_name) {
		this.trainer_name = trainer_name;
	}
	public int getBook_hour() {
		return book_hour;
	}
	public void setBook_hour(int book_hour) {
		this.book_hour = book_hour;
	}
	public int getBook_pay() {
		return book_pay;
	}
	public void setBook_pay(int book_pay) {
		this.book_pay = book_pay;
	}
	
	
}

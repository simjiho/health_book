package vo;

public class TrainerVO {
	private int trainer_id;
	private String trainer_passwd;
	private String trainer_name;
	private String trainer_major;
	private int pay;
	
	public int getTrainer_id() {
		return trainer_id;
	}
	public void setTrainer_id(int trainer_id) {
		this.trainer_id = trainer_id;
	}
	public String getTrainer_passwd() {
		return trainer_passwd;
	}
	public void setTrainer_passwd(String trainer_passwd) {
		this.trainer_passwd = trainer_passwd;
	}
	public String getTrainer_name() {
		return trainer_name;
	}
	public void setTrainer_name(String trainer_name) {
		this.trainer_name = trainer_name;
	}
	public String getTrainer_major() {
		return trainer_major;
	}
	public void setTrainer_major(String trainer_major) {
		this.trainer_major = trainer_major;
	}
	public int getPay() {
		return pay;
	}
	public void setPay(int pay) {
		this.pay = pay;
	}
	
	
}
